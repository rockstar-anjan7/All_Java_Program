package com.company;

public class swapping {
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.println("before com.company.swapping a= "+a);
        System.out.println("and b="+b);
        int t=a;
        a=b;
        b=t;
        System.out.println("after com.company.swapping a="+a);
        System.out.println("and b="+b);

    }
}
